// While loop:
//     - Repeats as long as the condition is true.
//     - Checks the condition before each iteration.
//     Syntax:
//     while (condition) {
//          // code to execute
//     }

#include<iostream>
using namespace std;
int main(){
    int i=1;
    while( i<=5){
        cout<<" Numbers "<<i<<endl;
        i++;
    }
return 0;
}